const projectEn = [velaProjectEn, hiromantProjectEn, translatorEn]
const projectRu = [velaProjectRu, hiromantProjectRu, translatorRu]
let projectsAll = projectEn
let i = 0
let j = projectRu.length - 1

const projects = function projects(state = [], action) {
	switch (action.type) {
		case 'NEXT_PROJECT': {
			i === j ? (i = 0) : i++
			const newState = projectsAll[i]
			return newState
		}
		case 'PREV_PROJECT': {
			i === 0 ? (i = j) : i--
			const newState = projectsAll[i]
			return newState
		}
		case 'CHANGE_LANGUAGE_TO_EN': {
			projectsAll = projectEn
			const newState = projectsAll[i]
			return newState
		}
		case 'CHANGE_LANGUAGE_TO_RU': {
			projectsAll = projectRu
			const newState = projectsAll[i]
			return newState
		}
		default: {
			const state = projectsAll[i]
			return state
		}
	}
}

export default projects
